#include"ft_printf.h"

int	ft_putstr(char *s)
{
	int	k;

	k = 0;
	if (s == NULL)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (*s)
	{
		k++;
		write (1, s++, 1);
	}
	return (k);
}
